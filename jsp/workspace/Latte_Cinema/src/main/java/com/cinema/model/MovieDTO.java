package com.cinema.model;

public class MovieDTO {

	private int movie_no;
	private String movie_title;
	private String movie_genre;
	private int movie_screen_time;
	private String movie_director;
	private String movie_actor;
	private String movie_release_date;
	private String movie_poster;
	private String movie_content;
	private int movie_limit_age;
	
	public int getMovie_no() {
		return movie_no;
	}
	public void setMovie_no(int movie_no) {
		this.movie_no = movie_no;
	}
	public String getMovie_title() {
		return movie_title;
	}
	public void setMovie_title(String movie_title) {
		this.movie_title = movie_title;
	}
	public String getMovie_genre() {
		return movie_genre;
	}
	public void setMovie_genre(String movie_genre) {
		this.movie_genre = movie_genre;
	}
	public int getMovie_screen_time() {
		return movie_screen_time;
	}
	public void setMovie_screen_time(int movie_screen_time) {
		this.movie_screen_time = movie_screen_time;
	}
	public String getMovie_director() {
		return movie_director;
	}
	public void setMovie_director(String movie_director) {
		this.movie_director = movie_director;
	}
	public String getMovie_actor() {
		return movie_actor;
	}
	public void setMovie_actor(String movie_actor) {
		this.movie_actor = movie_actor;
	}
	public String getMovie_release_date() {
		return movie_release_date;
	}
	public void setMovie_release_date(String movie_release_date) {
		this.movie_release_date = movie_release_date;
	}
	public String getMovie_poster() {
		return movie_poster;
	}
	public void setMovie_poster(String movie_poster) {
		this.movie_poster = movie_poster;
	}
	public String getMovie_content() {
		return movie_content;
	}
	public void setMovie_content(String movie_content) {
		this.movie_content = movie_content;
	}
	public int getMovie_limit_age() {
		return movie_limit_age;
	}
	public void setMovie_limit_age(int movie_limit_age) {
		this.movie_limit_age = movie_limit_age;
	}
}
