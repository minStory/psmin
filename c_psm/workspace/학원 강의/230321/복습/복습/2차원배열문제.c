//N* M크기의 두 행렬 A와 B가 주어졌을 때, 두 행렬을 더하는 프로그램을 작성하시오.
//
//입력
//첫째 줄에 행렬의 크기 N 과 M이 주어진다.둘째 줄부터 N개의 줄에 행렬 A의 원소 M개가 차례대로 주어진다.이어서 N개의 줄에 행렬 B의 원소 M개가 차례대로 주어진다.
//N과 M은 100보다 작거나 같고, 행렬의 원소는 절댓값이 100보다 작거나 같은 정수이다.
//
//출력
//첫째 줄부터 N개의 줄에 행렬 A와 B를 더한 행렬을 출력한다.행렬의 각 원소는 공백으로 구분한다.
//
//입력
//3 3
//{1, 1, 1} {3, 3, 3} -- > {(1 + 3), (1 + 3), (1 + 3)}
//2 2 2
//0 1 0
//
//3 3 3
//4 4 4
//5 5 100
//
//출력
//4 4 4
//6 6 6
//5 6 100

#include<stdio.h>
void main() {

	int A[100][100] = { 0 };		//100*100 행열 선언
	int B[100][100] = { 0 };

	int n, m;
	scanf_s("%d %d", &n, &m);	//행열 크기 입력

	for (int i = 0; i < n; i++) {		//행열A 값 입력
		for (int j = 0; j < m; j++) {
			scanf_s("%d", &A[i][j]);
		}
	}
	for (int i = 0; i < n; i++) {		//행열B 값 입력
		for (int j = 0; j < m; j++) {
			scanf_s("%d", &B[i][j]);
		}
	}
	for (int i = 0; i < n; i++) {		//행열A+B 값 출력
		for (int j = 0; j < n; j++) {
			printf("%d ", A[i][j] + B[i][j]);
		}
		printf("\n");
	}
}
